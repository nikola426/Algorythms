def heapify(arr, n, i):
    """Вспомогательная функция для преобразования массива в кучу."""
    largest = i  # Инициализируем наибольший элемент как корень
    left = 2 * i + 1  # Левый дочерний элемент
    right = 2 * i + 2  # Правый дочерний элемент

    # Если левый дочерний элемент больше корня
    if left < n and arr[left] > arr[largest]:
        largest = left

    # Если правый дочерний элемент больше наибольшего элемента
    if right < n and arr[right] > arr[largest]:
        largest = right

    # Если наибольший элемент не корень
    if largest != i:
        arr[i], arr[largest] = arr[largest], arr[i]  # Меняем местами

        # Рекурсивно преобразуем затронутое поддерево в кучу
        heapify(arr, n, largest)

def heap_sort(arr):
    """Основная функция для сортировки массива с помощью кучи."""
    n = len(arr)

    # Построение кучи (перегруппировка массива)
    for i in range(n // 2 - 1, -1, -1):
        heapify(arr, n, i)

    # Извлечение элементов из кучи один за другим
    for i in range(n - 1, 0, -1):
        arr[i], arr[0] = arr[0], arr[i]  # Меняем местами
        heapify(arr, i, 0)  # Перегруппируем кучу

# Пример использования
arr = [12, 11, 13, 5, 6, 7]
print("Исходный массив:", arr)

heap_sort(arr)
print("Отсортированный массив:", arr)
